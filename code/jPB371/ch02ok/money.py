print("{0:<8}{1:^5}{2:^5}{3:^5}{4:^5}{5:^5}{6:^5}{7:^5}". \
      format("name","day1","day2","day3", \
             "day4","day5","day6", \
             "day7"))
print("{0:<8}{1:^5}{2:^5}{3:^5}{4:^5}{5:^5}{6:^5}{7:^5}". \
      format(name,day1,day2,day3,day4,day5,day6,day7))
